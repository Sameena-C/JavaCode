package basicsprogram;

public class Cond_Op_if_else {

	public static void main(String[] args) {
		int age=45;
		//int age=12;
		if (age>=18)
		{
			System.out.println("You can Vote");
		}
		else
		{
			System.out.println("You cannot Vote");
		}

	}

}
