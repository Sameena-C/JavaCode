/*
 Program to print numbers from -1 to -5
 using for loop
 This program will not print anything to output because 
 -1 is bigger than -5 and for loop condition will be false 
 */
package basicsprogram;

public class For_Loop_Ex6 {

	public static void main(String[] args) {
		
		for(int i=-1; i<-5; i++) 
		{
			System.out.println(i);
		}

	}

}
